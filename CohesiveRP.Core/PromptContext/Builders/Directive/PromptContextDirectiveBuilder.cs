namespace CohesiveRP.Core.PromptContext.Builders.Directive
{
    public class PromptContextDirectiveBuilder : IPromptContextElementBuilder
    {
        private IStorageService storageService;
        private PromptContextFormatElement promptContextFormatElement;
        private ChatDbModel chatDbModel;

        public PromptContextDirectiveBuilder(IStorageService storageService, PromptContextFormatElement promptContextFormatElement, ChatDbModel chatDbModel)
        {
            this.storageService = storageService;
            this.promptContextFormatElement = promptContextFormatElement;
            this.chatDbModel = chatDbModel;
        }

        public async Task<(string, IShareableContextLink)> BuildAsync()
        {
            string userPersonaName = "Azariel";// TODO: fetch from Db
            return ($"# Directive{Environment.NewLine}{promptContextFormatElement?.Options?.Format?.Replace("{{user}}", userPersonaName)}", new ShareableContextLink{ LinkedBuilder = this });
        }
    }
}
